async function main() {
  logger.info('初始化 featured 用户中 ....');
  await prisma.$transaction(async (tx) => {
    for (let i = 0; i < featuredUsers.length; i += 1) {
      const user = featuredUsers[i];
      let userId;
      // eslint-disable-next-line no-await-in-loop
      if (user.mobile) {
        // eslint-disable-next-line no-await-in-loop
        const existUser = await tx.sys_user.findFirst({
          where: {
            username: user.mobile,
          },
        });
        if (!existUser) {
          // eslint-disable-next-line no-await-in-loop
          const snowflake = new Snowflake({
            datacenterId: 1, // 数据中心 ID（0-31 之间）
            workerId: 1, // 机器/节点 ID（0-31 之间）
          });
          const id = snowflake.nextId();
          // eslint-disable-next-line no-await-in-loop
          const result = await tx.sys_user.create({
            data: {
              id,
              username: user.mobile,
            },
          });
          userId = result.id;
        }
      }
      // eslint-disable-next-line no-await-in-loop
      const savedUser = await tx.User.create({
        data: {
          id: user.id,
          sys_user_id: userId,
          username: user.username,
          featured: user.featured,
        },
      });
      logger.info('初始化用户成功：', savedUser);
      // eslint-disable-next-line no-await-in-loop
      const profile = await tx.Profile.create({
        data: {
          id: user.profile.id,
          user_id: savedUser.id,
          name: user.profile.name,
          img: user.profile?.img || '',
          dob: user.profile.dob,
          bio: user.profile.bio,
          tts_model_id: user.profile.tts_model_id,
          tts_voice_id: user.profile.tts_voice_id,
          system_prompt: user.profile.system_prompt,
        },
      });
      logger.info('初始化用户个人资料成功：', profile);
    }
  });
}

main()
  .then(async () => {
    await prisma.$disconnect();
  })
  .catch(async (e) => {
    logger.error(e);
    await prisma.$disconnect();
    process.exit(1);
  });
